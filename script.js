function userData(gitUrl) {
  const xhr = new XMLHttpRequest();
  xhr.open('GET', gitUrl, true);
  xhr.onload = function () {
    if (xhr.status === 200) {
      const data = JSON.parse(xhr.responseText);
      console.log(data);
      displayData(data);
    } else {
      console.error('err', xhr.status);
    }
  };
  xhr.onerror = function () {
    console.error('Request failed');
  };
  xhr.send();
}

